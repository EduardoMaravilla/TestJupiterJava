package org.eduardomaravill.classes;

public class Sum {

    public int operationSum(int a ,int b){
        return a + b;
    }
    public double operationSum(double a ,double b){
        return a + b;
    }
    public float operationSum(float a ,float b){
        return a + b;
    }
    public long operationSum(long a ,long b){
        return a + b;
    }
}
